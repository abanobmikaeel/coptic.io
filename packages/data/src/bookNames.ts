const AR_DIGITS = ['٠', '١', '٢', '٣', '٤', '٥', '٦', '٧', '٨', '٩']
const toArabicDigits = (value: string): string =>
	value.replace(/[0-9]/g, (d) => AR_DIGITS[Number(d)])

const toLang = (lang: string): BookNameLang => (lang === 'ar' ? 'ar' : lang === 'es' ? 'es' : 'en')

/** Localized Bible book name (canonical English → target language). */
export const getBibleBookName = (englishName: string, lang = 'en'): string =>
	BIBLE_BOOK_NAMES[toLang(lang)]?.[englishName] ?? englishName

const EN_BOOK_KEYS = Object.keys(BIBLE_BOOK_NAMES.en)

/**
 * Localize the book name(s) in a verse reference string such as
 * "Psalms 99:6-7" or "Psalms 65:11;Psalms 81:1", keeping chapter/verse numbers.
 */
export const localizeReference = (ref: string | undefined, lang = 'en'): string => {
	if (!ref) return ''
	const l = toLang(lang)
	if (l === 'en') return ref
	const sep = l === 'ar' ? '؛ ' : '; '
	return ref
		.split(';')
		.map((segment) => {
			const part = segment.trim()
			const book = EN_BOOK_KEYS.filter((b) => part.startsWith(b)).sort(
				(a, b) => b.length - a.length,
			)[0]
			if (!book) return l === 'ar' ? toArabicDigits(part) : part
			const rest = part.slice(book.length)
			return getBibleBookName(book, l) + (l === 'ar' ? toArabicDigits(rest) : rest)
		})
		.join(sep)
}
